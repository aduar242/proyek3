    /**
     * Remove the specified resource from storage.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function destroy($id)
    {
        $clients = Client::findOrFail($id);
        $clients->delete();
        redirect()->back()->with(['success' => '<strong>' . $clients->name . '</strong> Telah Dihapus!']);
